package javaapplication5;

/**
 *
 * @author iTs
 */
public class Countries {
    
    private String code;
    private String name;
    private String contenet;

    public Countries(String code, String name, String contenet) {
        this.code = code;
        this.name = name;
        this.contenet = contenet;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public String getContenet() {
        return contenet;
    }

 


    
    
}
